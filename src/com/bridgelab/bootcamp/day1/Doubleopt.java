package com.bridgelab.bootcamp.day1;

public class Doubleopt {

	public static void main(String[] args) {
		double a=10.5,b=5.5,c=2.0;
		System.out.println("a+b*c="+(a+b*c));
		System.out.println("a*b+c="+(a*b+c));
		System.out.println("c+a/b="+(c+a/b));
		System.out.println("a%b+c="+(a%b+c));
	}

}
